public static String  calculadora(String memoria1, String memoria2,String signos){
    Double resultado=0.0;
    String respuesta;

    if (signos.equals("-")){
        resultado=Double.parseDouble(memoria1)- Double.parseDouble(memoria2);
    }
    if (signos.equals("+")){
        resultado=Double.parseDouble(memoria1)+ Double.parseDouble(memoria2);
    }
    if (signos.equals("*")){
        resultado=Double.parseDouble(memoria1)* Double.parseDouble(memoria2);
    }
    if (signos.equals("/")){
        resultado=Double.parseDouble(memoria1)/ Double.parseDouble(memoria2);
    }
    respuesta=resultado.toString();
    return respuesta;
}
        public static boolean existepunto(String cadena){
    boolean resultado;
    resultado = false;
    for (int i=0; i< cadena.length(); i++){
    
        if (cadena.substring(i, i+1).equals(".")){
        resultado= true;
        break;
    }
}
    return resultado;
}
    
